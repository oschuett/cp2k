                       +cell%hmat(3, 2)*local_shift(2) &
                       +cell%hmat(3, 3)*local_shift(3)
         IF (PRESENT(lgridr)) THEN
            CALL collocGauss(h=cell%hmat, h_inv=cell%h_inv, &
